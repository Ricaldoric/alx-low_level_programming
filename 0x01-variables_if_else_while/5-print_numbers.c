#include <stdio.h>

/**
 * main - program entry point
 *
 * description: prints all single digit numbers of base 10 starting from 0
 * followed by a new line
 *
 * Return: Always (0), success
 */
int main(void) /*start of the prohgram*/
{
	for (int i; i < 10; i++)
	{
		printf("%d", i);
	}
	printf(" \n");


	return (0);
}


